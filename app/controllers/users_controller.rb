  #注册验证
  def ajax
    @email=params[:email]
    @step=params[:step]

    #验证是否可用
    if @step=='checkemail'
      user=User.find_by_email(@email)
      if user
        render json: {error:1,msg:"该帐号已经被注册了!"}
      else
        render json: {error: 0}
      end
    end

    #发送激活邮件
    if @step=='sendmail'
      render :template  => 'users/_reg_active_mail.html',:layout => false
    end

  end
end
