        <div class="con_title"><h2 class="name">详细介绍</h2></div>
        一部与北漂有关的小说。没有从天而降的好爹，没有半路杀出的救星，只是对真实北漂生活的诚实记录，几个普通青年的徒劳的挣扎。是回到平淡乏味的老家，还是留在喧嚣浮躁的北京？难道没有第三条路可以选吗？我们的生活还有没有希望？有没有希望……
作者过去的作品以带有悬疑的奇幻小说为主，也许是在幻想的天空里飘得太久了，他想重回真实的大地。于是，他写了一部现代都市题材的长篇小说，他想要描述的只是生活的真实面目，但是真实生活并不比幻想世界里的荒诞与残酷更少，或许，更多。
唐缺，现居京城，无业游民，卖文为生。曾以笔名“雨夜屠夫”发表科幻小说若干，自2006年开始奇幻创作，以九州世界作品为主，已创作九州作品逾两百万字，有单行本《英雄》《星痕》《云之彼岸》《轮回之悸》等出版，风格自成一家，以“反英雄主义”精神游走于严谨与虚幻之间，擅长以幽默调侃的文字与诡诈多变的悬疑布局拨动读者心弦。参与编剧武侠喜剧电影《刀见笑》，于2011年上映，获得第四十八届金马奖最佳改编剧本提名。
